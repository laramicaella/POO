    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        jTextArea1.setText("");
        int [] nmr_ins = new int[aux.getLocais().size()];
        int n,i,z,k=0,max=0,y;
        //System.out.println("imprimir nmr locais->"+aux.getLocais().size());
        for(z=0; z<aux.getLocais().size(); z++){
            n = aux.llocais(aux.getLocais().get(z));
            nmr_ins[k] = n;//coloca o nmr de pessoas inscritas no local (por ordem no array de locais)
            k++;
            
            if (aux.getLocais().get(z).local_bar() == false)
                jTextArea1.append(aux.getLocais().get(z).getNome_local()+" -> Nmr inscritos: "+n+"\n");
            else if(aux.getLocais().get(z).local_bar() == true)
                jTextArea1.append(aux.getLocais().get(z).getNome_local()+" -> Nmr inscritos: "+n+"  Lotação maxima: " + aux.getLocais().get(z).lotacao()+"\n");
        }
        
        jTextArea1.append("\n -------- Locais ordenados por ordem decrecente --------\n");
        for(z=0; z<aux.getLocais().size(); z++)
        {
            for (i = 0; i < nmr_ins.length; i++)
            {
                if (nmr_ins[i] > max) 
                    max = nmr_ins[i];
            }
            //System.out.println(max);
            for (y = 0; y < nmr_ins.length; y++)
            {
                if (nmr_ins[y] == max)
                { 
                    jTextArea1.append(aux.getLocais().get(y).getNome_local() + "\n");
                    nmr_ins[y] = -1;
                }
            }
            max = 0;
        }
    }//GEN-LAST:event_jButton3ActionPerformed

