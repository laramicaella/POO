    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            int nmr, i, a=0;
            char[] pass;
            char[] p = new char[] {'n', 'u', 'l', 'l'};
            nmr = Integer.parseInt(Nmr_inscrever.getText());
            pass = Pass_inscrever.getPassword();

            for(i=0; i<aux.getPessoas().size(); i++){
                if((aux.getPessoas().get(i).getNmr() == nmr)){//e o nmr existe nas Pessoas_Dei
                    a=1;
                    if ((Arrays.equals(aux.getPessoas().get(i).getPal_chave(), p)) ){//E a palavra pass esta a null
                        aux.escolher_pass(nmr, pass);
                        Res1.setText("Password definida com sucesso!");
                        Pass_inscrever.setText("");
                        Nmr_inscrever.setText("");
                        Pass_inscrever.setText("");
                        break;
                    }
                    else{
                        Res1.setText("A pessoa já tem uma password definida!");
                        Nmr_inscrever.setText("");
                        break;                        
                    }
                }      
            }
            if(a!=1){
                    Res1.setText("Número não existe!");
                    Nmr_inscrever.setText("");
                }
        } catch (NumberFormatException e) {
            Res1.setText("Numero com caracteres invalido!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

